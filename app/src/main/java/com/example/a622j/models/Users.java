package com.example.a622j.models;

public class Users {

    String fullname;
    String email;
    String password;
    String c_password;

    public Users(String fullname, String email, String password, String c_password) {
        this.fullname = fullname;
        this.email = email;
        this.password = password;
        this.c_password = c_password;
    }
}
